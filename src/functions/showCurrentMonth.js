export let showCurrentMonth = () => {
    const months = [
        "January",
        "February",
        "March",
        "April",
        "May",
        "June",
        "July",
        "August",
        "September",
        "October",
        "November",
        "December",
    ];

    let monthsName = months[new Date().getMonth()];
    return monthsName;
}